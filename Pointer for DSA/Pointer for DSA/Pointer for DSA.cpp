#include<iostream>
using namespace std;
int main() {

	int a = 10;
	int *p = &a;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Address of a: " << &a << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of a: " << a << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Address of p: " << &p << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of p: " << p << endl; // the address of the a and the p is same 
	//becuase they store the address of the a.
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of *p: " << *p << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	// using the dereference operator.
	int** q = &p;
	cout << "Address of p: " << &p << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of p: " << p << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Address of q: " << &q << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of q: " << q << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of *q: " << *q << endl;
	cout << "***********************************\n";
	cout << "***********************************\n";
	cout << "Value of **q: " << **(q) << endl;




	return 0;
}